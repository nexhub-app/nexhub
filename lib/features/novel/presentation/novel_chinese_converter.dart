/// 简→繁 字符映射表（基于 [_t2sMap] 反向生成，多对一时取最后写入项）。
final Map<String, String> _s2tMap = <String, String>{
  for (final MapEntry<String, String> e in _t2sMap.entries)
    e.value: e.key,
};

/// 按指定模式转换字符串。
///
/// - [ChineseConvertMode.none] 原样返回。
/// - [ChineseConvertMode.traditionalToSimplified] 逐字查 [_t2sMap]。
/// - [ChineseConvertMode.simplifiedToTraditional] 逐字查 [_s2tMap]。
String convertChinese(String input, ChineseConvertMode mode) {
  if (mode == ChineseConvertMode.none || input.isEmpty) return input;
  final map = mode == ChineseConvertMode.traditionalToSimplified
      ? _t2sMap
      : _s2tMap;
  if (map.isEmpty) return input;
  final buffer = StringBuffer();
  for (final ch in input.runes) {
    final s = String.fromCharCode(ch);
    buffer.write(map[s] ?? s);
  }
  return buffer.toString();
}

/// 批量转换段落列表（返回新列表，原列表不变）。
List<String> convertChineseList(
  List<String> input,
  ChineseConvertMode mode,
) {
  if (mode == ChineseConvertMode.none || input.isEmpty) {
    return List<String>.from(input, growable: false);
  }
  return input
      .map((String p) => convertChinese(p, mode))
      .toList(growable: false);
}
